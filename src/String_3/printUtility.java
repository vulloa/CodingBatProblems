public class printUtility {

    public static void printString(String s)
    {
        System.out.println(s + "\n");
    }

    public static void printInt(int i)
    {
        System.out.println(i + "\n");
    }

    public static void printChar(char c)
    {
        System.out.println(c + "\n");
    }

}
